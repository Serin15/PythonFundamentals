#Basic Operators:
#Python supports all basic arithmetic, comparison, and logical operators.
# Arithmetic operations
a = 10
b = 3
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division (float)
print(a // b)  # Division (int)
print(a % b)  # Modulo
print(a ** b)  # Exponentiation

# Comparison operators
print(a > b)  # Greater than
print(a == b)  # Equality

# Logical operators
print(True and False)
print(True or False)
print(not True)
