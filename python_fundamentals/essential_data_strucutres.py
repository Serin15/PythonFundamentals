#List:
#lists are ordered and mutable collections.

my_list = [1, 2, 3, 4]
my_list.append(5)
print(my_list[0])  # Access the first element
print(len(my_list))  # Get the length of the list

#Dictionary:
#dictionaries are collections of key_value pairs.
my_dict = {"name": "Alex", "age": 25}
print(my_dict["name"])  # Access value by key

#Tuple:
#tuples are immutable (cannot be changed after creation).
my_tuple = (1, 2, 3)
print(my_tuple[0])
